#include "shared.h"

void	replace_a_b(char ***opers)
{
	int	i;

	i = 0;
	while (*opers != NULL && (*opers)[i] != NULL)
	{
		(*opers)[i][ft_strlen((*opers)[i]) - 1] = 'b';
		i++;
	}
}
